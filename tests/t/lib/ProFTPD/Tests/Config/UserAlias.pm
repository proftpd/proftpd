sub useralias_and_anon_issue831 {
  my $self = shift;
  my $tmpdir = $self->{tmpdir};
  my $setup = test_setup($tmpdir, 'config');

  my $alias = 'foobarbaz';

  # Create a second user for anonymous logins
  auth_user_write($setup->{auth_user_file}, 'ftpd', 'ftpd', $setup->{uid},
    $setup->{gid}, $setup->{home_dir}, '/bin/bash');
  auth_group_write($setup->{auth_group_file}, 'ftpd', $setup->{gid}, 'ftpd');

  my $config = {
    PidFile => $setup->{pid_file},
    ScoreboardFile => $setup->{scoreboard_file},
    SystemLog => $setup->{log_file},

    AuthUserFile => $setup->{auth_user_file},
    AuthGroupFile => $setup->{auth_group_file},
    AuthOrder => 'mod_auth_file.c',

    UserAlias => "$alias $setup->{user}",

    IfModules => {
      'mod_delay.c' => {
        DelayEngine => 'off',
      },
    },
  };

  my ($port, $config_user, $config_group) = config_write($setup->{config_file},
    $config);

  if ($ENV{TEST_VERBOSE}) {
    print STDERR "# User $config_user, Group $config_group, Port $port\n";
  }

  if (open(my $fh, ">> $setup->{config_file}")) {
    print $fh <<EOC;
<Anonymous $tmpdir>
  User ftpd
  Group ftpd
  UserAlias anonymous ftpd
</Anonymous>
EOC
    unless (close($fh)) {
      die("Can't write $setup->{config_file}: $!");
    }

  } else {
    die("Can't open $setup->{config_file}: $!");
  }

  # Open pipes, for use between the parent and child processes.  Specifically,
  # the child will indicate when it's done with its test by writing a message
  # to the parent.
  my ($rfh, $wfh);
  unless (pipe($rfh, $wfh)) {
    die("Can't open pipe: $!");
  }

  my $ex;

  # Fork child
  $self->handle_sigchld();
  defined(my $pid = fork()) or die("Can't fork: $!");
  if ($pid) {
    eval {
      my $client = ProFTPD::TestSuite::FTP->new('127.0.0.1', $port);
      my ($resp_code, $resp_msg) = $client->user($alias);
      $client->pass($setup->{passwd});
      $client->quit();

      my $expected = 331;
      $self->assert($resp_code == $expected,
        test_msg("Expected response code $expected, got $resp_code"));

      $expected = "Password required for $alias";
      $self->assert($resp_msg eq $expected,
        test_msg("Expected response message '$expected', got '$resp_msg'"));

      # Now log in again, this time anonymously
      $client = ProFTPD::TestSuite::FTP->new('127.0.0.1', $port);
      ($resp_code, $resp_msg) = $client->user('anonymous');
      $client->pass('NoRealPasswordNeededHere');
      $client->quit();

      $expected = 331;
      $self->assert($resp_code == $expected,
        test_msg("Expected response code $expected, got $resp_code"));

      $expected = 'Anonymous login ok, send your complete email address as your password';
      $self->assert($resp_msg eq $expected,
        test_msg("Expected response message '$expected', got '$resp_msg'"));
    };
    if ($@) {
      $ex = $@;
    }

    $wfh->print("done\n");
    $wfh->flush();

  } else {
    eval { server_wait($setup->{config_file}, $rfh) };
    if ($@) {
      warn($@);
      exit 1;
    }

    exit 0;
  }

  # Stop server
  server_stop($setup->{pid_file});
  $self->assert_child_ok($pid);

  test_cleanup($setup->{log_file}, $ex);
}


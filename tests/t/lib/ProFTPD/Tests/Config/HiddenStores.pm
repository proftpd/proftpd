sub find_hiddenstore_files {
  my $path = shift;
  my $prefix = shift;

  my $dirh;
  unless (opendir($dirh, $path)) {
    die("Can't open directory '$path': $!");
  }

  my $files  = [grep { /^$prefix/ && -f "$path/$_" } readdir($dirh)]; 
  closedir($dirh); 

  return $files;
}

